Digital Circuits

Let's start with a small description language for digital circuits. A digital circuit is composed of *wires* and of
functional components. Wires transport signals that are transformed by components. We represent signals using booleans
true and false.

The base components (gates) are:

► The Inverter, whose output is the inverse of its input.
► The AND Gate, whose output is the conjunction of its inputs.
► The OR Gate, whose output is the disjunction of its inputs.

Other components can be constructed by combining these base components.

The components have a reaction time (or *delay*), i.e. their outputs don't change immediately after a change to their
inputs

